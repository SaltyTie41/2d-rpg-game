// Player class
class Player {
private:

    int heroHealth = 50;
    int heroVitality = 10;
    int heroStrength = 10;
    int heroAgility = 10;
    int xp = 0;
    int lvl = 1;
    int skillPoints = 5;
    std::string name = "Player";
    int choice = 0;


public:
    sf::RectangleShape shape;

    Player(int x, int y) {
        shape.setSize(sf::Vector2f(TILE_SIZE, TILE_SIZE));
        shape.setFillColor(sf::Color::Green);
        shape.setPosition(sf::Vector2f(x * TILE_SIZE, y * TILE_SIZE));
    }

    void takeDamage(int damage) {
        heroHealth = std::max(0, heroHealth - damage);
    }

    void giveXP(int a) {
        xp += a;
    }

    void levelCheck()
    {

        if (xp > ((lvl * 0.5) * 100))
        {
            xp -= (lvl * 0.5) * 100;
            lvl += 1;
            skillPoints += 7;
            system("cls");
            std::cout << "You leveled up!\n\n";
            displayStats();
            std::this_thread::sleep_for(std::chrono::seconds(2));
        }
    }

    int getChoice() {
        int x;
        std::cin >> x;
        return x;
    }

    void setSkills()
    {


        while (skillPoints > 0)
        {
            system("cls");
            displayStats();
            std::cout << "\nYou have " << skillPoints << " skill points to spend." << std::endl << "What will you spend it on?:\n1. Strength\n2. Agility\n3. Vitality\n";
            choice = getChoice();
            switch (choice)
            {
            case 1:
                skillPoints -= 1;
                heroStrength += 1;
                break;
            case 2:
                skillPoints -= 1;
                heroAgility += 1;
                break;
            case 3:
                skillPoints -= 1;
                heroVitality += 1;
                break;
            default:
                std::cout << "Wrong Choice";
            }
            system("cls");
        }
    }

    void applyStats()
    {
        heroHealth = 20 + (heroVitality - 10); // Player's health based on vitality
    }


    // Function to display the stats
    void displayStats() {
        std::cout << "Stats:\n";
        std::cout << "Level: " << lvl << std::endl;
        std::cout << "XP: " << xp << std::endl;
        std::cout << "Strength: " << heroStrength << std::endl;
        ///  cout << "2. Perception: " << perception << endl;
        std::cout << "Agility: " << heroAgility << std::endl;
        std::cout << "Vitality: " << heroVitality << std::endl;
        std::cout << std::endl;
    }

    int getHealth() {
        return heroHealth;
    }

    float getXPosition() {
        sf::Vector2f pos = shape.getPosition();
        return pos.x;
    }
    float getYPosition() {
        sf::Vector2f pos = shape.getPosition();
        return pos.y;
    }


};

