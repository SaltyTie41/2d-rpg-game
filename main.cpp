template <typename T>
T getChoice() {
    T x;
    std::cin >> x;
    return x;
}




class Entity {
protected:
    int health;
public:
    virtual int getHealth() = 0;
    Entity(int x) {
        health = x;
    }
};


class Item {
protected:
    int id;
public:
    Item(int i) {
        id = i;
    }
    virtual int getId() = 0;
};

class Weapon : public Item {
protected:
    int damage;
public:
    Weapon(int d, int i) : Item(i) {
        damage = d;
    }
    int getId() override {
        return id;
    }
    int getDamage() {
        return damage;
    }
};

class Armor : public Item {
protected:
    int defense;
public:
    Armor(int d, int i) : Item(i) {
        defense = d;
    }
    int getId() override {
        return id;
    }
    int getDefense() {
        return defense;
    }
};

class Equipment : public Weapon, public Armor {
protected:
    int addedHealth;
    int addedAttack;
public:
    Equipment(int damage, int defense, int i) : Weapon(damage, i), Armor(defense, i) {
        addedAttack = getDamage();
        addedHealth = getDefense();
    }
    int getId() override {
        return 0;
    }

};





// Player class
class Player : public Entity, public Equipment {
private:


    int heroVitality = 10;
    int heroStrength = 10;
    int heroAgility = 10;
    int xp = 0;
    int lvl = 1;
    int skillPoints = 5;
    std::string name = "Player";
    int choice = 0;


public:
    sf::RectangleShape shape;

    Player(int x, int y) : Entity(50), Equipment(10, 10, 0) {
        shape.setSize(sf::Vector2f(TILE_SIZE, TILE_SIZE));
        shape.setFillColor(sf::Color::Green);
        shape.setPosition(sf::Vector2f(x * TILE_SIZE, y * TILE_SIZE));
        heroStrength += addedAttack;
        health += addedHealth;
    }

    void takeDamage(int damage) {
        health = std::max(0, health - damage);
    }

    void giveXP(int a) {
        xp += a;
    }

    void levelCheck()
    {

        if (xp > ((lvl * 0.5) * 100))
        {
            xp -= (lvl * 0.5) * 100;
            lvl += 1;
            skillPoints += 7;
            system("cls");
            std::cout << "You leveled up!\n\n";
            displayStats();
            std::this_thread::sleep_for(std::chrono::seconds(2));
        }
    }



    void setSkills()
    {


        while (skillPoints > 0)
        {
            system("cls");
            displayStats();
            std::cout << "\nYou have " << skillPoints << " skill points to spend." << std::endl << "What will you spend it on?:\n1. Strength\n2. Agility\n3. Vitality\n";
            choice = getChoice<int>();
            switch (choice)
            {
            case 1:
                skillPoints -= 1;
                heroStrength += 1;
                break;
            case 2:
                skillPoints -= 1;
                heroAgility += 1;
                break;
            case 3:
                skillPoints -= 1;
                heroVitality += 1;
                break;
            default:
                std::cout << "Wrong Choice";
            }
            system("cls");
        }
    }

    void applyStats()
    {
        health += (heroVitality - 10); // Player's health based on vitality
    }


    // Function to display the stats
    void displayStats() {
        std::cout << "Stats:\n";
        std::cout << "Level: " << lvl << std::endl;
        std::cout << "XP: " << xp << std::endl;
        std::cout << "Strength: " << heroStrength << std::endl;
        ///  cout << "2. Perception: " << perception << endl;
        std::cout << "Agility: " << heroAgility << std::endl;
        std::cout << "Vitality: " << heroVitality << std::endl;
        std::cout << std::endl;
    }

    int getHealth() override {
        return health;
    }

    float getXPosition() {
        sf::Vector2f pos = shape.getPosition();
        return pos.x;
    }
    float getYPosition() {
        sf::Vector2f pos = shape.getPosition();
        return pos.y;
    }


};

